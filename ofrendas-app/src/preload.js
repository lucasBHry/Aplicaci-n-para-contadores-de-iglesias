const { contextBridge, ipcRenderer } = require('electron');
contextBridge.exposeInMainWorld('api', {
  configGet: (key) => ipcRenderer.invoke('config:get', key),
  configSet: (key, value) => ipcRenderer.invoke('config:set', key, value),
  setupCheck: () => ipcRenderer.invoke('setup:check'),
  setupCreate: (data) => ipcRenderer.invoke('setup:create', data),
  login: (data) => ipcRenderer.invoke('auth:login', data),
  contadoresList: () => ipcRenderer.invoke('contadores:list'),
  contadoresAdd: (data) => ipcRenderer.invoke('contadores:add', data),
  contadoresEdit: (data) => ipcRenderer.invoke('contadores:edit', data),
  contadoresDelete: (id) => ipcRenderer.invoke('contadores:delete', id),
  contadoresToggle: (id) => ipcRenderer.invoke('contadores:toggle', id),
  diasList: () => ipcRenderer.invoke('dias:list'),
  diasCreate: (data) => ipcRenderer.invoke('dias:create', data),
  diasGet: (id) => ipcRenderer.invoke('dias:get', id),
  diasSave: (data) => ipcRenderer.invoke('dias:save', data),
  diasDelete: (id) => ipcRenderer.invoke('dias:delete', id),
  diasAddRows: (data) => ipcRenderer.invoke('dias:add-rows', data),
  comentariosAdd: (data) => ipcRenderer.invoke('comentarios:add', data),
  personasList: (year) => ipcRenderer.invoke('personas:list', year),
  personasYears: () => ipcRenderer.invoke('personas:years'),
  personasData: (data) => ipcRenderer.invoke('personas:data', data),
  personasAddFila: (data) => ipcRenderer.invoke('personas:add-fila', data),
  pdfSave: (data) => ipcRenderer.invoke('pdf:save', data),
  xlsxSave: (data) => ipcRenderer.invoke('xlsx:save', data),
  xlsxGenerate: (data) => ipcRenderer.invoke('xlsx:generate', data),
  logoPick: () => ipcRenderer.invoke('logo:pick'),
  logoRemove: () => ipcRenderer.invoke('logo:remove'),
  adminChangePassword: (data) => ipcRenderer.invoke('admin:change-password', data),
  backupSave: () => ipcRenderer.invoke('backup:save'),
  backupRestore: (data) => ipcRenderer.invoke('backup:restore', data),
});